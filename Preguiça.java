package exercicioherançaepolimorfismo;

public class Preguiça extends animal{
	
	private String estado;
	private int peso;
	private Double dataselo;
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
	public Double getDataselo() {
		return dataselo;
	}
	public void setDataselo(double d) {
		this.dataselo = (double) d;
	}
	
	
	

}
